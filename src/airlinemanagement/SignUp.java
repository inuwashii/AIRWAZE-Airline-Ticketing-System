    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        String Fname = fnametxt.getText();
        String Uemail = emailtxt.getText();
        String Pword = new String(passtxt.getPassword());
        String passport = passporttxt.getText();
        String country = (String) countrytxt.getSelectedItem();
        String contact = contacttxt.getText();
        String gender = (String) gendertxt.getSelectedItem();
        if (Fname.isEmpty()|| Uemail.isEmpty()|| Pword.isEmpty()||passport.isEmpty()||country.isEmpty()||contact.isEmpty()){
            JOptionPane.showMessageDialog(this, "All fields must be filled");
        } else {
        
        try {
            pst=conn.prepareStatement("INSERT INTO users (fname, email, password, passportID, contact, country,gender) VALUES(?,?,?,?,?,?,?)");
            pst.setString(1, Fname);
            pst.setString(2, Uemail);
            pst.setString(3, Pword);
            pst.setString(4, passport);
            pst.setString(5, contact);
            pst.setString(6, country);
            pst.setString(7, gender);
            
        
            int k=pst.executeUpdate();

            if(k == 1) {
                passporttxt.setText("");
                fnametxt.setText("");
                emailtxt.setText("");
                contacttxt.setText("");
                passtxt.setText("");

                JOptionPane.showMessageDialog(this, "SUCCESFULLY REGISTERED");  
            } 
        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_registerBtnActionPerformed

